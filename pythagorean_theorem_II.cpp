#include<bits/stdc++.h>
using namespace std;

void solve()
{
	int n;
	cin >> n;
	
	int cnt=0;
	for(int a=1;a<=n;a++)
	{
		for(int b=a;b<=n;b++)
		{
			int c=(a*a)+(b*b);
			int sq = sqrt(c);

			if(sq*sq == c && sq <= n)
				cnt++;
		}
	}
	cout << cnt;
}

