#include<bits/stdc++.h>
using namespace std;

void solve()
{
	int n;
	cin >> n;
	int n2=n;
	
	int arr[n];

	for(int i=0;i<n2;i++)
	{
		arr[i]=n--;
	}

	if(n2&1)
		swap(arr[n2/2],arr[n2-1]);

	for(auto x:arr)
		cout << x << " ";
}

